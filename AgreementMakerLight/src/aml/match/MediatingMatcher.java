package aml.match;

import java.io.File;
import java.util.Set;

import aml.AML;
import aml.ontology.Lexicon;
import aml.ontology.Ontology;
import aml.settings.LexicalType;

public class MediatingMatcher extends LexicalMatcher implements LexiconExtender
{

//Attributes

	//The external ontology used as a mediator
	private Lexicon ext;
	private String uri;
	//The type of lexical entry generated by this Lexicon extender
	private final LexicalType TYPE = LexicalType.EXTERNAL_MATCH;
	
//Constructors

	/**
	 * Constructs a MediatingMatcher with the given external Ontology
	 * @param x: the external Ontology
	 */
	public MediatingMatcher(Ontology x)
	{
		ext = x.getLexicon();
		uri = x.getURI();
	}
	
	/**
	 * Constructs a MediatingMatcher with the given external Lexicon file
	 * @param file: the file with the external Lexicon
	 */
	public MediatingMatcher(String file)
	{
		try
		{
			ext = new Lexicon(file);
		}
		catch(Exception e)
		{
			System.out.println("Unable to build lexicon: " + file);
			e.printStackTrace();
			ext = new Lexicon();
		}
		uri = (new File(file)).getName();
	}

//Public Methods
	
	@Override
	public void extendLexicons(double thresh)
	{
		System.out.println("Extending Lexicons with Mediating Matcher using " + uri);
		long time = System.currentTimeMillis()/1000;
		AML aml = AML.getInstance();
		Lexicon source = aml.getSource().getLexicon();
		Alignment src = match(source,ext,thresh,true);
		for(Mapping m : src)
		{
			int t = m.getTargetId();
			Set<String> names = ext.getNames(t);
			for(String n : names)
			{
				double sim = m.getSimilarity() * ext.getWeight(n, t);
				if(sim >= thresh)
					source.addClass(m.getSourceId(), n, TYPE, uri, sim);
			}
		}
		Lexicon target = aml.getTarget().getLexicon();
		Alignment tgt = match(target,ext,thresh,true);
		for(Mapping m : tgt)
		{
			int t = m.getTargetId();
			Set<String> names = ext.getNames(t);
			for(String n : names)
			{
				double sim = m.getSimilarity() * ext.getWeight(n, t);
				if(sim >= thresh)
					target.addClass(m.getSourceId(), n, TYPE, uri, sim);
			}
		}
		time = System.currentTimeMillis()/1000 - time;
		System.out.println("Finished in " + time + " seconds");
	}
	
	@Override
	public Alignment match(double thresh)
	{
		System.out.println("Running Mediating Matcher using " + uri);
		long time = System.currentTimeMillis()/1000;
		AML aml = AML.getInstance();
		Lexicon source = aml.getSource().getLexicon();
		Lexicon target = aml.getTarget().getLexicon();
		Alignment src = match(source,ext,thresh,true);
		Alignment tgt = match(target,ext,thresh,true);
		Alignment maps = new Alignment();
		for(Mapping m : src)
		{
			int sourceId = m.getSourceId();
			int medId = m.getTargetId();
			Set<Integer> matches = tgt.getTargetMappings(medId);
			for(Integer j : matches)
			{
				double similarity = Math.min(m.getSimilarity(),
						tgt.getSimilarity(j, medId));
				maps.add(new Mapping(sourceId,j,similarity));
			}
		}
		time = System.currentTimeMillis()/1000 - time;
		System.out.println("Finished in " + time + " seconds");
		return maps;
	}
}